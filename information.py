import streamlit as st

st.title("Information on Papaya Ripeness Stages")
st.write("""
Papayas go through different stages of ripeness, which can be identified based on their color and texture:
- **Unripe**: Green skin, hard flesh.
- **Partially Ripe**: Yellow-green skin, softer flesh.
- **Fully Ripe**: Yellow skin, soft flesh, sweet aroma.
- **Overripe**: Orange or yellow skin with dark spots, very soft flesh, strong aroma.
""")
